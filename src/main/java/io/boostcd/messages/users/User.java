package io.boostcd.messages.users;


public class User {

	private String name;
	
	private String uid;


	public String getName() {
		return name;
	}

	public String getUid() {
		return uid;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setUid(String uid) {
		this.uid = uid;
	}

}
